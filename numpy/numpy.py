"""
NumPy Array Basics Example

This script demonstrates basic NumPy array creation and element access.
"""

import numpy as np

# Create a numpy array
a = np.array([0, 1, 2, 3, 4])

# Print each element
print("a[0]:", a[0])
print("a[1]:", a[1])
print("a[2]:", a[2])
print("a[3]:", a[3])
print("a[4]:", a[4])
